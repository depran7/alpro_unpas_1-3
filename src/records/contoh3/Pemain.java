public class Pemain {
    private String nama;
    private int noPunggung;
    private int gaji;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getNoPunggung() {
        return noPunggung;
    }

    public void setNoPunggung(int noPunggung) {
        this.noPunggung = noPunggung;
    }

    public int getGaji() {
        return gaji;
    }

    public void setGaji(int gaji) {
        this.gaji = gaji;
    }
    
}
